    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (controlarCampos()) {
            try {
                Funcion funcion = new Funcion(
                        Integer.parseInt(ID.getText()),
                        Date.valueOf(Fecha.getText()),
                        Time.valueOf(Hora.getText()),
                        Integer.parseInt(Sala.getText())
                );
                FileManager.guardarFuncion(funcion, "funciones.txt");
                JOptionPane.showMessageDialog(this, "Funci√≥n guardada para pel√≠cula con id: " + funcion.getIdPelicula());
                dispose();
            } catch (IOException | IllegalArgumentException ex) {
                JOptionPane.showMessageDialog(this, "Error al guardar la funci√≥n. Introduzca los datos correctamente");
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

